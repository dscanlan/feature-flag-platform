#!/usr/bin/env node

import { promises as fs } from "node:fs";
import path from "node:path";

const rootDir = process.cwd();
const workspaceConfigPath = path.join(rootDir, "pnpm-workspace.yaml");
const outputArg = process.argv[2];
const outputPath = outputArg
  ? path.resolve(rootDir, outputArg)
  : path.join(rootDir, "docs", "dependency-graph.mmd");

const ignoredDirectories = new Set([
  ".git",
  ".turbo",
  ".cache",
  "build",
  "coverage",
  "dist",
  "node_modules",
]);

const dependencyFields = [
  "dependencies",
  "devDependencies",
  "peerDependencies",
  "optionalDependencies",
];

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exitCode = 1;
});

async function main() {
  const workspacePatterns = await readWorkspacePatterns(workspaceConfigPath);
  const packageFiles = await findPackageFiles(rootDir);
  const workspaces = await loadWorkspaces(packageFiles, workspacePatterns);

  if (workspaces.length === 0) {
    throw new Error("No workspaces found from pnpm-workspace.yaml patterns.");
  }

  const graph = buildWorkspaceGraph(workspaces);
  const content = renderMermaid(graph);

  await fs.mkdir(path.dirname(outputPath), { recursive: true });
  await fs.writeFile(outputPath, content, "utf8");

  console.log(
    `Wrote dependency graph for ${graph.workspaces.length} workspaces to ${path.relative(
      rootDir,
      outputPath,
    )}`,
  );
}

async function readWorkspacePatterns(filePath) {
  const content = await fs.readFile(filePath, "utf8");

  return content
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter((line) => line.startsWith("- "))
    .map((line) => {
      const value = line.slice(2).trim();
      return value.replace(/^["']|["']$/g, "");
    })
    .filter(Boolean);
}

async function findPackageFiles(directory) {
  const entries = await fs.readdir(directory, { withFileTypes: true });
  const packageFiles = [];

  for (const entry of entries) {
    const fullPath = path.join(directory, entry.name);

    if (entry.isDirectory()) {
      if (ignoredDirectories.has(entry.name)) {
        continue;
      }

      packageFiles.push(...(await findPackageFiles(fullPath)));
      continue;
    }

    if (entry.isFile() && entry.name === "package.json") {
      packageFiles.push(fullPath);
    }
  }

  return packageFiles;
}

async function loadWorkspaces(packageFiles, workspacePatterns) {
  const matchers = workspacePatterns.map(toMatcher);
  const workspaces = [];

  for (const filePath of packageFiles) {
    if (filePath === path.join(rootDir, "package.json")) {
      continue;
    }

    const relativeDir = toPosixPath(path.relative(rootDir, path.dirname(filePath)));

    if (!matchers.some((matcher) => matcher(relativeDir))) {
      continue;
    }

    const manifest = JSON.parse(await fs.readFile(filePath, "utf8"));

    if (!manifest.name) {
      continue;
    }

    workspaces.push({
      name: manifest.name,
      dir: relativeDir,
      group: relativeDir.split("/")[0] ?? "workspace",
      manifest,
    });
  }

  return workspaces.sort((left, right) => left.dir.localeCompare(right.dir));
}

function buildWorkspaceGraph(workspaces) {
  const nameToWorkspace = new Map(workspaces.map((workspace) => [workspace.name, workspace]));
  const edges = [];

  for (const workspace of workspaces) {
    const edgeTypesByTarget = new Map();

    for (const field of dependencyFields) {
      const dependencies = workspace.manifest[field] ?? {};

      for (const dependencyName of Object.keys(dependencies)) {
        if (!nameToWorkspace.has(dependencyName) || dependencyName === workspace.name) {
          continue;
        }

        const types = edgeTypesByTarget.get(dependencyName) ?? new Set();
        types.add(field);
        edgeTypesByTarget.set(dependencyName, types);
      }
    }

    for (const [target, types] of edgeTypesByTarget) {
      edges.push({
        from: workspace.name,
        to: target,
        types: [...types].sort(),
      });
    }
  }

  edges.sort((left, right) => {
    const bySource = left.from.localeCompare(right.from);
    if (bySource !== 0) {
      return bySource;
    }

    return left.to.localeCompare(right.to);
  });

  return { workspaces, edges };
}

function renderMermaid(graph) {
  const nodeIds = new Map(
    graph.workspaces.map((workspace, index) => [workspace.name, `workspace_${index + 1}`]),
  );
  const groups = new Map();

  for (const workspace of graph.workspaces) {
    const items = groups.get(workspace.group) ?? [];
    items.push(workspace);
    groups.set(workspace.group, items);
  }

  const lines = [
    "%% Generated by scripts/draw-dependency-graph.mjs",
    "flowchart LR",
    "  classDef apps fill:#e8f4ff,stroke:#2563eb,color:#0f172a;",
    "  classDef packages fill:#ecfccb,stroke:#65a30d,color:#1a2e05;",
    "  classDef infra fill:#ffe7c2,stroke:#d97706,color:#431407;",
    "  classDef workspace fill:#f3f4f6,stroke:#6b7280,color:#111827;",
  ];

  for (const [groupName, workspaces] of groups) {
    lines.push(
      `  subgraph ${sanitizeIdentifier(`group_${groupName}`)}["${escapeLabel(groupName)}"]`,
    );

    for (const workspace of workspaces) {
      const nodeId = nodeIds.get(workspace.name);
      const label = `${workspace.name}<br/>${workspace.dir}`;
      const className = ["apps", "packages", "infra"].includes(workspace.group)
        ? sanitizeIdentifier(workspace.group)
        : "workspace";
      lines.push(`    ${nodeId}["${escapeLabel(label)}"]:::${className}`);
    }

    lines.push("  end");
  }

  if (graph.edges.length > 0) {
    lines.push("");

    for (const edge of graph.edges) {
      const fromId = nodeIds.get(edge.from);
      const toId = nodeIds.get(edge.to);
      const label = edge.types.join(", ");
      lines.push(`  ${fromId} -->|"${escapeLabel(label)}"| ${toId}`);
    }
  }

  lines.push("");
  return `${lines.join("\n")}\n`;
}

function toMatcher(pattern) {
  const normalized = toPosixPath(pattern);
  const escaped = normalized.replace(/[|\\{}()[\]^$+?.]/g, "\\$&");
  const regexSource = `^${escaped.replace(/\*\*/g, ".*").replace(/\*/g, "[^/]+")}$`;
  const regex = new RegExp(regexSource);

  return (value) => regex.test(value);
}

function toPosixPath(value) {
  return value.split(path.sep).join("/");
}

function sanitizeIdentifier(value) {
  return value.replace(/[^a-zA-Z0-9_]/g, "_");
}

function escapeLabel(value) {
  return value.replaceAll('"', "&quot;");
}
